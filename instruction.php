<h3>A. General information:<br></h3>
1. The examination will comprise of Objective type Multiple Choice Questions (MCQs)<br>
2. All questions are compulsory and each carries One mark.<br>
3. The total number of questions, duration of examination, will be different based on
the course, the detail is available on your screen.<br>
4. The Subjects or topics covered in the exam will be as per the Syllabus.<br>
5. There will be NO NEGATIVE MARKING for the wrong answers.<br>
<h3>B. Information & Instructions:<br></h3>
1. The examination does not require using any paper, pen, pencil and calculator.<br>
2. Every student will take the examination on a Laptop/Desktop/Smart Phone<br>
3. On computer screen every student will be given objective type type Multiple Choice
Questions (MCQs).<br>
4. Each student will get questions and answers in different order selected randomly
from a fixed Question Databank.<br>
5. The students just need to click on the Right Choice / Correct option from the
multiple choices /options given with each question. For Multiple Choice Questions,
each question has four options, and the candidate has to click the appropriate
option.<br>

6. The Time of the examination begins only when the ‘Start Test’ button is pressed.
7. The student proceeds answering the questions one by one by clicking on the small
grey circle next to the chosen answer.<br>
